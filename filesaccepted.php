<h1>Filetypes</h1>
<p>This tool will accept submissions in multiple formats. If not specified, it will attempt to
determine the filetype autmatically.  This does not always work.  If you know the type, please
do specify it.  If you cannot find a filetype that matches your data, please still submit
without specifying a type.  We will do our best to work it out for you.</p>

